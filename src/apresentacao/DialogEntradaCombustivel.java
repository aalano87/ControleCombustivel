    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        obj = this.getDados();
        //  System.out.println(obj.getNf() + "  "+ obj.getFornecedor());
        if (verificaNF(obj.getNf(), obj.getFornecedor().getNome()) == false) {
            try {
                gerenciador.inserir(obj);
                this.limparCampos();
            } catch (ExcecaoConexao ex) {
                JOptionPane.showMessageDialog(this, "Falha na conex√£o com o banco de dados.\n"
                        + "Msg.: " + ex.getMessage(), "Cadastro de Entrada", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ExcecaoSQL ex) {
                JOptionPane.showMessageDialog(this, "Falha durante a grava√ß√£o do registro.\n"
                        + "Msg.: " + ex.getMessage(), "Cadastro de Entrada", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane, "Dados salvos com sucesso!");
            btSalvar.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "NF j√° cadastrada.");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        obj = this.getDados();
        try {
            gerenciador.atualizar(obj);
            this.limparCampos();
        } catch (ExcecaoConexao ex) {
            JOptionPane.showMessageDialog(this, "Falha na conex√£o com o banco de dados.\n"
                    + "Msg.: " + ex.getMessage(), "Cadastro de NF", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoSQL ex) {
            JOptionPane.showMessageDialog(this, "Falha durante a atualiza√ß√£o dos dados no Banco de Dados.\n"
                    + "Msg.: " + ex.getMessage(), "Cadastro de NF", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Dados Atualizados com sucesso!");
    }//GEN-LAST:event_btAtualizarActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        obj = this.getDados();
        try {
            if (JOptionPane.showConfirmDialog(this,
                    "Remover NF " + obj.getNf() + " Fornecedor " + obj.getFornecedor(),
                    "Cadastro de NF", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE) == 0) {
                gerenciador.remover(obj);
                this.limparCampos();
                JOptionPane.showMessageDialog(null, "NF removida com sucesso!");
            }
        } catch (ExcecaoConexao ex) {
            JOptionPane.showMessageDialog(this, "Falha na conex√£o com o banco de dados.\n"
                    + "Msg.: " + ex.getMessage(), "Cadastro de NF", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoSQL ex) {
            JOptionPane.showMessageDialog(this, "Falha na remo√ß√£o deste registro.\n"
                    + "Msg.: " + ex.getMessage(), "Cadastro de NF", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

