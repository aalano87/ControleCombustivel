    private void limparCampos() {
        tfCodigo.setText(null);
        jDate.setDate(null);
        tfMotorista.setText(null);
        tfKmHs.setText(null);
        tfLitros.setText(null);
        cbPlaca.setSelectedIndex(-1);
        carregarID();
        btSalvar.setEnabled(true);
    }

    private Abastecimento getDados() {
        if (obj == null) {
            obj = new Abastecimento();
        }
        obj.setVeiculo((Veiculo) cbPlaca.getSelectedItem());
        obj.setData(jDate.getDate());
        if (obj.getVeiculo().getTipo().equals("Caminhão") || obj.getVeiculo().getTipo().equals("Utilitário")) {
            obj.setKm(Integer.valueOf(tfKmHs.getText()));
            obj.setHoras(0);
        } else if (obj.getVeiculo().getTipo().equals("Máquina")) {
            obj.setHoras(Integer.valueOf(tfKmHs.getText()));
            obj.setKm(0);
        }
        obj.setMotorista(tfMotorista.getText().toUpperCase());
        obj.setLitros(Float.valueOf(tfLitros.getText()));
        return obj;
    }

    private void setDados(Abastecimento obj) {
        this.jDate.setDate(obj.getData());
        this.tfKmHs.setText(String.valueOf(obj.getHoras()));
        this.tfKmHs.setText(String.valueOf(obj.getKm()));
        this.tfMotorista.setText(obj.getMotorista());
        this.tfLitros.setText(String.valueOf(obj.getLitros()));
        this.cbPlaca.setSelectedItem(obj.getVeiculo());
        this.tfCodigo.setText(String.valueOf(obj.getId()));
    }

    private boolean verificaAbastecimento(Date data, String placa, float litros) {
        try {
            if (gerenciador.ValorExistente(data, placa, litros) == false) {
                return false;
            }
        } catch (ExcecaoSQL | ExcecaoConexao | SQLException ex) {
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    }

    private void carregarDadosVeiculoCombo() {
        GerenciadorVeiculo gv = new GerenciadorVeiculo();
        try {
            lista = gv.obterTodos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Nenhum veículo cadastrado.");
        }
        cbPlaca.setModel(
                new DefaultComboBoxModel(lista.toArray()));
        cbPlaca.setSelectedIndex(-1);
    }

    public ImageIcon criarImageIcon(String caminho, String descricao) {
        java.net.URL imgURL = getClass().getResource(caminho);
        if (imgURL != null) {
            return new ImageIcon(imgURL, descricao);
        } else {
            System.err.println("Não foi possível carregar o arquivo de imagem: " + caminho);
            return null;
        }
    }

    public void estoqueLitros() {
        float x = 0;
        try {
            x = gerenciador.getLitrosEntrada() - gerenciador.getLitrosSaida();
        } catch (ExcecaoConexao ex) {
            Logger.getLogger(FrmTelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmTelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoSQL ex) {
            Logger.getLogger(FrmTelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        NumberFormat nb = new DecimalFormat("0.##");

        lbSaldoDisponivel.setText(String.valueOf(nb.format(x)));
    }

