    private void limparCampos() {
        try {
            tfCodigo.setText(String.valueOf(gv.lastID()));
        } catch (SQLException ex) {
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoConexao ex) {
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoSQL ex) {
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
        tfPlaca.setValue(null);
        tfPlaca.setEditable(true);
        tfVeiculo.setText(null);
        cbStatus.setSelectedIndex(-1);
        cbTipo.setSelectedIndex(-1);
        cbProprietario.setSelectedIndex(-1);
    }

    private Veiculo getDados() {
        if (v == null) {
            v = new Veiculo();
        }
        v.setVeiculo(tfVeiculo.getText().toUpperCase());
        String placa = tfPlaca.getText().replace("-", "");
        v.setPlaca(placa);
        v.setTipo(cbTipo.getSelectedItem().toString());
        v.setStatus(cbStatus.getSelectedItem().toString());
        v.setProprietario((Proprietario)cbProprietario.getSelectedItem());
        return v;
    }

    private void setDados(Veiculo v) {
        this.tfCodigo.setText(String.valueOf(v.getId()));
        this.tfPlaca.setText(v.getPlaca());
        this.tfVeiculo.setText(v.getVeiculo());
        this.cbStatus.setSelectedItem(v.getStatus());
        this.cbTipo.setSelectedItem(v.getTipo());
        this.cbProprietario.setSelectedItem(v.getProprietario());
    }

    private boolean verificaPlaca(String placa) {
        try {
            if (gv.ValorExistente(placa) == false) {
                return false;
            }
        } catch (ExcecaoSQL | ExcecaoConexao | SQLException ex) {
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    }

    public ImageIcon criarImageIcon(String caminho, String descricao) {
        java.net.URL imgURL = getClass().getResource(caminho);
        if (imgURL != null) {
            return new ImageIcon(imgURL, descricao);
        } else {
            System.err.println("Não foi possível carregar o arquivo de imagem: " + caminho);
            return null;
        }
    }

    private void carregarDadosProprietarioCombo() {
        GerenciadorProprietario gp = new GerenciadorProprietario();
        try {
            lista = gp.obterTodos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Nenhum veículo cadastrado.");
        }
        cbProprietario.setModel(
                new DefaultComboBoxModel(lista.toArray()));
        cbProprietario.setSelectedIndex(-1);
    }
    
