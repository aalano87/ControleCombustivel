    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        v = this.getDados();
        try {
            if (JOptionPane.showConfirmDialog(this,
                    "Remover o veiculo " + v.getVeiculo() + " Placas " + v.getPlaca(),
                    "Cadastro de Veiculo", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE) == 0) {
                gv.remover(v);
                this.limparCampos();
                JOptionPane.showMessageDialog(null, "Veículo removido com sucesso!");
            }
        } catch (ExcecaoConexao ex) {
            JOptionPane.showMessageDialog(this, "Falha na conexão com o banco de dados.\n"
                    + "Msg.: " + ex.getMessage(), "Cadastro de Veiculo", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoSQL ex) {
            JOptionPane.showMessageDialog(this, "Falha na remoção deste registro.\n"
                    + "Msg.: " + ex.getMessage(), "Cadastro de Veiculo", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(DialogCadastroVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

