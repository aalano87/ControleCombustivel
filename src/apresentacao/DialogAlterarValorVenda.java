    private void configurarTabela() {
        try {
            this.setListaValorVenda(gv.obterTodos());
        } catch (ExcecaoConexao ex) {
            JOptionPane.showMessageDialog(this, "Falha ao estabelecer a conexão com o banco de dados");
            Logger.getLogger(DialogAlterarValorVenda.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ExcecaoSQL ex) {
            JOptionPane.showMessageDialog(this, "Erro na recuperação dos dados.");
            Logger.getLogger(DialogAlterarValorVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        tableValorVenda.setAutoCreateColumnsFromModel(false);
        java.awt.FontMetrics fm = tableValorVenda.getFontMetrics(tableValorVenda.getFont());
        tableValorVenda.setColumnModel(new ValorVendaColumnModel(fm));
        tableValorVenda.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public void setListaValorVenda(java.util.List<ValorVenda> valores) {
        this.tableValorVenda.setModel(new ValorVendaTableModel(valores));
    }
