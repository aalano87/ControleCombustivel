    private ValorVenda getDados() {
        if (v == null) {
            v = new ValorVenda();
        }
        v.setValor(Double.valueOf(tfNovoValor.getText()));
        v.setDatainserido(dtNovaDataReajuste.getDate());

        return v;
    }

    private void setDados(ValorVenda v) {
        this.tfValorAnterior.setText(String.valueOf(v.getValor()));
        this.dtUltimaModificacao.setDate(v.getDatainserido());

    }


